/* map an ASCII char to a glyph index (uppercase folded) */
static u8 glyph_of(char ch) {
  if (ch >= 'a' && ch <= 'z') ch -= 32;
  if (ch == ' ') return 0;
  if (ch >= 'A' && ch <= 'Z') return (u8)(1 + (ch - 'A'));
  if (ch >= '0' && ch <= '9') return (u8)(27 + (ch - '0'));
  switch (ch) {
    case ':': return 37; case '!': return 38; case '.': return 39;
    case '-': return 40; case '>': return 41; case '(': return 42;
    case ')': return 43; case '/': return 44; case '+': return 45;
    case '%': return 46; case '\'': return 47; case ',': return 48;
  }
  return 0; /* unknown -> space */
}

